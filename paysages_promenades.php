<p> ➜ Les paysages urbains : entre ensembles patrimoniaux et grandes agglomérations :</p>
<p>Ils représentent 8 % de la superficie de la région. Lieux de patrimoine ou secteurs dégradés et banalisés, ils sont le cadre de vie de la plupart des habitants de la région. Les villages perchés, les parcs, bastides et jardins sont autant d’éléments majeurs de l’identité provençale et azuréenne.</p>
<p>Le patrimoine historique et les monuments occupent une place prépondérante dans le paysage urbain, à l’exemple du palais des papes en Avignon, Notre-Dame-de-la-Garde à Marseille, ou encore les arènes d’Arles. Les sites traditionnels d’implantation humaine (littoral et couloir rhodanien) présentent d’importants secteurs protégés. Les territoires de montagne sont dotés de sites prestigieux comme les deux fortifications de Vauban : Mont-Dauphin et Briançon, patrimoine mondial de l’UNESCO.</p>
<p>Certains villages à la structure ou à l’organisation caractéristiques (villages perchés) ont su préserver leur patrimoine paysager et culturel : les Baux-de-Provence, Gordes, Moustiers-Sainte-Marie, Bormes-les-Mimosas, Saint-Paul-de-Vence, Eze, Embrun, etc.<br>

Le Vieux Port de Marseille. (© Laurent Mignaux/METL-MEDDE)</p>


<p>Pourtant cette structure identitaire, constituée d’un bâti groupé en situation dominante, perd parfois en lisibilité lorsque les villages s’étalent le long des coteaux pour répondre à la pression d’urbanisation. Les jardins et parcs publics, les parcs des bastides, les jardins familiaux contribuent à la diversité des demandes sociales et sont des lieux de découverte. Depuis 2006, le nombre de jardins labellisés remarquables est passé de 20 à 42. On compte parmi ces jardins remarquables le jardin du prieuré de Salagon, le domaine du Rayol, le jardin d’Albertas, le domaine de Charance, le vallon du Brec…</p>

<p>En revanche, les principales villes se sont développées le long des infrastructures, en englobant les hameaux alentours pour devenir de grosses agglomérations, voire de véritables métropoles dans les départements littoraux. Les zones d’activités, zones commerciales et zones industrielles se sont développées sans souci de structuration ou de création architecturale, avec le cortège d’altérations paysagères classiques : consommation dispersée de l’espace, minéralisation, standardisation, enseignes et publicité.</p>


