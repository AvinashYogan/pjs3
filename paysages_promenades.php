					<h1 style="text-align: center;">Paysages et promenades</h1>
					</br>
					<p>La région PACA est d’une exceptionnelle richesse paysagère. Terre de contrastes, elle présente une mosaïque de paysages à caractère montagnard et méditerranéen :

  <li>  chaînes de hautes montagnes au nord et à l’est jusqu’à la Méditerranée ;</li>

    <li>succession de collines et de plateaux de massifs essentiellement calcaires au centre ;</li>

<li>    vallées du Rhône et de la Durance, grandes plaines (Comtat, Crau, Camargue), étangs et grands lacs ;</li>

    <li>plaines littorales peu étendues à l’est.</li>

 

Ces paysages diversifiés ont été façonnés par le vent, l’eau et les civilisations successives. Les caractéristiques physiques et climatiques, la variété des altitudes, des climats et des sols déterminent la diversité végétale : du mélèze à l’olivier, des pelouses d’alpages aux plantes exotiques. Les paysages sont aussi le résultat des modes d’occupation des sols, d’habitat et d’exploitation agricole.
