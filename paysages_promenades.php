<h2>1.1. Une mosaïque de paysages regroupée en cinq grandes zones paysagères :</h2>

<p>Les caractéristiques physiques géomorphologiques et climatiques conduisent à une typologie en 5 grandes zones :</p>

<p>➜ Alpes du Sud</p>
<p>Paysages fortement contrastés avec des sommets à pelouses et à névés et des versants très boisés. La haute montagne (alpages, névés, glaciers) accueille des stations de sports d’hiver et des espaces labellisés Parc National (Écrins, Mercantour) ou Parc Naturel Régional (Queyras). Dans la moyenne montagne prédomine l’agriculture : terres de labours, arboriculture, prairies.</p>

<p>➜ Haute Provence ou moyen pays</p>
<p>Structurée par la limite climatique de l’olivier, elle correspond à une succession de plateaux et montagnes sèches traversées par la Durance, le Verdon, l’Asse, La Bléone et leurs principaux affluents. Ces rivières ont façonné des paysages exceptionnels au relief très marqué et à la géologie diversifiée : gorges du Verdon, cluses diverses attirant un tourisme important. C’est la zone de la lavande.</p>

<p>➜ Basse Provence ou collines provençales</p>
<p>Cet espace est marqué par des monts de faible altitude (Petit Luberon, Alpilles, Sainte-Victoire, Caussols…) constitue une zone de passage privilégié, donc plus peuplée. Les paysages prennent ici une valeur souvent emblématique typiquement provençale, liée à l’histoire et à l’usage des terroirs (exemple des Alpilles). Le patrimoine bâti fait la part belle aux villages perchés.</p>

<p>➜ Plaines provençales et zones littorales</p>
<p>Elles offrent un fort contraste avec les zones précédentes : plaines agricoles du Comtat à trame bocagère (haies et canaux d’irrigation), plaine de la Crau, bassin de l’étang de Berre, Camargue, littoral urbanisé et littoral naturel. Il s’agit de paysages exceptionnels liés à la douceur climatique et à la variété géologique affirmée : Provence calcaire à garrigues, pins d’Alep et chênes verts ; Provence cristalline à maquis, pins parasols et chênes lièges ; Côte d’Azur à végétation acclimatée ; empreinte du Rhône à l’ouest.</p>

<p>➜ Provence cristalline</p>
<p>Elle est constituée de milieux originaux se développant sur des massifs anciens : Estérel d’origine volcanique, Maures et Tannerons aux reliefs compartimentés et émoussés. Le maquis, la suberaie (chêne liège), la châtaigneraie sont les formations végétales propres à ces types de sols acides d’un pays peu habité, sauf au sud en corniche, et le long des golfes.</p>

 

