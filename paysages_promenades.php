<h2>1.2. Les caractéristiques des paysages de PACA :</h2>

<p>➜ Des paysages naturels et ruraux emblématiques :</p>

<p>La diversité des espaces naturels et ruraux porte l’identité régionale, à la fois provençale, azuréenne et alpine. On inventorie une vingtaine de grands paysages emblématiques dont certains sont classés au titre des paysages :</p>

<li>La Camargue, plus grande zone humide d’Europe : Au sein des paysages de Camargue, "fille du Rhône", se déclinent selon une subtile combinaison, l’eau, le ciel, l’horizontale des sols aux franges indécises. La Camargue fait l’objet d’un Parc Naturel Régional, d’un site classé, d’une réserve naturelle et d’une réserve de biosphère.</li>

<li>Le massif des Alpilles, bloc escarpé qui témoigne de civilisations anciennes. Sa valeur patrimoniale et économique est reconnue par une directive de protection et de mise en valeur du paysage.</li>

<li>L’ensemble du pays du Ventoux est dominé par le mont Ventoux (1 912 m), massif emblématique de Provence. Il comprend aussi une partie du Comtat Venaissin, le plateau de Sault, la vallée du Toulourenc et les dentelles de Montmirail. Il est inscrit en réserve de biosphère.</li>

<li> Le massif du Luberon est certes peu élevé (1 125 m au Mourre Nègre), mais possède un relief fortement marqué qui expose chacun de ses versants sud et nord aux influences méditerranéennes et alpines. Il est le cœur du Parc Naturel Régional qui porte son nom et est également inscrit en réserve de biosphère.</li>

<li> La montagne Sainte-Victoire, bloc calcaire culminant à plus de 1000 m, a été rendue mondialement célèbre par l’œuvre de Paul Cézanne. Outre sa grande richesse culturelle, naturelle et paysagère, elle constitue un espace récréatif privilégié dans un contexte de pression périurbaine très forte. Ce patrimoine majeur est site classé et bénéficie d’une Opération grand site (OGS). Il a obtenu le renouvellement en 2011 du label « grand site de France ».</li>

<li> Le massif des Calanques est connu pour ses grandes falaises calcaires tombant dans la mer. Au milieu de ce vaste espace naturel, quelques criques à cabanons (Les Goudes, Sormiou, Morgiou) présentent un bâti et un mode de loisirs originaux à proximité d’une ville de près de 1 million d’habitants. Il a été classé en parc national en 2012, le seul parc continental, marin et périurbain d’Europe.</li>

<li> La Sainte-Baume, vaste poumon vert d’une exceptionnelle richesse culturelle, écologique et paysagère au contact des aires marseillaise et toulonnaise, constitue le dernier grand site emblématique de Basse Provence non encore protégé. La création d’un PNR est en projet.</li>

<li>Les îles d’Hyères (Porquerolles, Port-Cros, Le Levant) attirent un public nombreux à la recherche d’une nature terrestre et marine unique sur le littoral.Le parc national de Port Cros est le 1er parc marin de Méditerranée.</li>



<li> Les gorges du Verdon : Ce « grand canyon » de 21 km de long constitue le seul événement de cette ampleur et de cette qualité en Europe. Le Verdon a creusé un lit encaissé délimité par de gigantesques falaises de roches calcaires. Cet espace en relation avec le lac de Sainte-Croix, est couvert par un Parc Naturel Régional. Il est site classé et fait l’objet d’une Opération grand site.</li>

<li>La chaîne des Maures, s’étend de Hyères à Fréjus. Ce vaste massif est une île cristalline au cœur de la Provence calcaire. Il convient de lui adjoindre la plaine des Maures, avec laquelle il forme une même entité géographique.</li>

<li> L’Estérel, caractérisé par des roches volcaniques rouges et sa côte rocheuse très découpée, est un immense massif boisé qui offre autant de richesse en biodiversité qu’en paysages.</li>

<li> Les plateaux de Caussols et Calern, et de Vence /Saint-Barnabé : ces hauts plateaux sauvages en balcon sur la Côte d’Azur figurent parmi les plus grands karsts d’Europe.</li>

<li>La Durance est l’axe majeur et l’âme de la Provence. Trait d’union régional des montagnes hautes alpines aux plaines du Comtat, elle constitue une voie historique de circulation des hommes et des bêtes. Cette terre agricole fertile est une zone d’attraction humaine, industrielle, commerciale, un réservoir d’eau, d’énergie et un milieu naturel exceptionnel.</li>

<li>Les Écrins : massif le plus élevé de la région à plus de 4 100 m, au paysage unique, dont les hauteurs sont faites de pierres et rochers, éboulis et torrents, glaces et neiges éternelles.</li>

<li>Le Mercantour : Ce massif côtoie la Méditerranée ce qui lui confère une grande diversité de paysages : site rocheux, vallées et sites glaciaires parsemés de lac, pâturages d’altitude, forêts denses, vallées étroites et gorges. Le Mercantour est célèbre pour le site rupestre de la vallée des Merveilles.</li>

<li>Le Queyras, ses paysages hérités de l’ère glaciaire, vallées en auge, épaulements, verrous, vallées suspendues, gorges. Ils sont marqués par le rythme des saisons. L’architecture reflète l’influence de chaque vallée, des rigueurs climatiques et contraintes topographiques. (Saint-Véran est le plus haut village d’Europe).</li>
<li>La vallée de la Clarée : considérée comme l’une des plus belles vallées habitées des Hautes-Alpes. Haut lieu de la randonnée et du ski de fond, ce site touristique majeur au cœur du Briançonnais est l’un des plus grands sites classés de France.</li>

<li>Le Dévoluy : plateau perché de montagne au caractère sauvage. Les plateaux agricoles aux pentes douces s’opposent aux montagnes et abritent un habitat dispersé traditionnel.</li>
